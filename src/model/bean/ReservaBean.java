package model.bean;

public class ReservaBean {
    private int monMatricula;
    private String locCodigo;
    private String dataHora;

    public int getMonMatricula() {
        return monMatricula;
    }

    public void setMonMatricula(int monMatricula) {
        this.monMatricula = monMatricula;
    }

    public String getLocCodigo() {
        return locCodigo;
    }

    public void setLocCodigo(String locCodigo) {
        this.locCodigo = locCodigo;
    }

    public String getDataHora() {
        return dataHora;
    }

    public void setDataHora(String dataHora) {
        this.dataHora = dataHora;
    }
    
    
}
